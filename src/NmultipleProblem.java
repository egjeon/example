public class NmultipleProblem {
    public int solution(int num, int n) {
        int answer = 0;
        if(num%n == 0)
            return 1;

        return answer;
    }
    public static void main(String[] args) {
        NmultipleProblem np = new NmultipleProblem();
        System.out.println(np.solution(2, 3));
    }
}
